//스와이퍼 click event
var count = 0;
function checkClick() {
  var color = ['#CFD8DC','#F3B743'];
  var bg = document.getElementById('click1').style;
  if (count%2==0) {
    bg.backgroundColor = color[1];
    count+=1;
  }
  else { 
    bg.backgroundColor = color[0];
    count+=1;
  }
}
